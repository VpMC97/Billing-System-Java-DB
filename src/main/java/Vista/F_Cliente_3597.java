    private void LimpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) TablaCliente.getModel();
        modelo.getDataVector().removeAllElements();
        TablaCliente.updateUI();
        
        TableData();
    }
    
    private void TableData(){
        
        String sql = "select * from Clientes;";
        
        try {
            Statement comando = c_conexion3597.establecerConexion().createStatement();
            ResultSet registro = comando.executeQuery(sql);
            
            while (registro.next()){
                DefaultTableModel modelo = (DefaultTableModel) TablaCliente.getModel();
                Object[] fila = new Object[8];
                fila[0] = registro.getString("codigo");
                fila[1] = registro.getString("NIT");
                fila[2] = registro.getString("nombre");
                fila[3] = registro.getString("apellido");
                fila[4] = registro.getString("DPI");
                fila[5] = registro.getString("direccion");
                fila[6] = registro.getString("edad");
                fila[7] = registro.getString("telefono");
                modelo.addRow(fila);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            c_conexion3597.closeConexion();
        }
    }
    
    
