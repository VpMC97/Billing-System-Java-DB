    private void bttBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBuscarActionPerformed

        String cod = JOptionPane.showInputDialog("Ingrese código de cliente a buscar: ");
        String sql = "select * from Clientes where codigo =" + cod + ";";
        
        try {
            Statement consulta = c_conexion3597.establecerConexion().createStatement();
            ResultSet registro = consulta.executeQuery(sql);
            
            if (registro.next()){
                lbCODIGO.setText(registro.getString("codigo"));
                txNIT.setText(registro.getString("NIT"));
                txNombre.setText(registro.getString("nombre"));
                txApellido.setText(registro.getString("apellido"));
                txDPI.setText(registro.getString("DPI"));
                txDirección.setText(registro.getString("direccion"));
                txEdad.setText(registro.getString("edad"));
                txTelefono.setText(registro.getString("telefono"));
            }
            else{
                JOptionPane.showMessageDialog(null, "Código de Cliente no existente");
            }
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "No se pudo realizar esta acción: " + e);
        } finally {
            c_conexion3597.closeConexion();
        }
    }//GEN-LAST:event_bttBuscarActionPerformed

