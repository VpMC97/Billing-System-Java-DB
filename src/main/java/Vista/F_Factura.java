    private void bttAgregar_ArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAgregar_ArticuloActionPerformed
        if (txNumero_Caja.getText().isEmpty() || txIDcajero.getText().isEmpty() || lbNombreEmpleado.getText() == null ||
            lbCodEmpleado.getText() == null || txNITcliente.getText().isEmpty() || lbNombreCliente.getText() == null ||
            lbCodigoCliente.getText() == null || txCodArticulo.getText().isEmpty() || lbNombreArticulo.getText() == null ||
            lbPrecioUnitario.getText() == null || txCantidad.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Se necesitan todos los campos llenos para completar esta acción.\nInténtelo de nuevo");
        }
        else{
            if (ab_Articulo3597.VArticuloFac(parseInt(txCodArticulo.getText())) != -1){
                int i = ab_Articulo3597.VArticuloFac(parseInt(txCodArticulo.getText()));
                A = ab_Articulo3597.A_inv();
                Double total = parseInt(txCantidad.getText()) * parseDouble(lbPrecioUnitario.getText());

                Articulo_3597 a = new Articulo_3597(parseInt(txCodArticulo.getText()), 
                                                    A.get(i).getNombre(), 
                                                    Double.toString(total), 
                                                    parseInt(txCantidad.getText()), 
                                                    A.get(i).getPrecioUnitario(), 
                                                    A.get(i).getCantidad());

                if (ab_Factura3597.VerificarArticulo(a) == - 1){
                    if (A.get(i).getCantidad() >= a.getCantidad()){
                        ab_Factura3597.G_Array(a);
                        DefaultTableModel modelo = (DefaultTableModel) TablaArticulo.getModel();
                        Object[] fila = new Object[5];
                        fila[0] = a.getCodigo();
                        fila[1] = a.getCantidad();
                        fila[2] = a.getNombre();
                        fila[3] = a.getPrecioUnitario();
                        fila[4] = a.getDescripción();
                        modelo.addRow(fila);

                        a.setcProveedor(a.getcProveedor() - a.getCantidad());
                        limpiarArticulo();                        
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Cantidad de Articulo: '" + A.get(i).getNombre() + 
                                                    "' insuficiente para realizar esta acción.\nStock: " + A.get(i).getCantidad());
                        txCantidad.setText("");
                        txCantidad.requestFocus();                    
                    }
                }
                else{
                    art = ab_Factura3597.A_DetalleArticulos();
                    int p = ab_Factura3597.VerificarArticulo(a);

                    if (art.get(p).getcProveedor() >= a.getCantidad()){
                        art.get(p).setCantidad(art.get(p).getCantidad() + a.getCantidad());
                        art.get(p).setDescripción(Double.toString(parseDouble(a.getDescripción())+ parseDouble(art.get(p).getDescripción())));
                        art.get(p).setcProveedor(art.get(p).getcProveedor() - a.getCantidad());

                        TablaArticulo.setValueAt(art.get(p).getCantidad(), p, 1);
                        TablaArticulo.setValueAt(art.get(p).getDescripción(), p, 4);
                        limpiarArticulo(); 
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Cantidad de Articulo: '" + A.get(i).getNombre() + 
                                                    "'\n insuficiente para realizar esta acción.\nStock: " + art.get(p).getcProveedor());
                        txCantidad.setText("");
                        txCantidad.requestFocus();                           
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Artículo no existente\nCódigo de artículo no coincide con los demás campos.");
                limpiarArticulo();
                }
        }
    }//GEN-LAST:event_bttAgregar_ArticuloActionPerformed

