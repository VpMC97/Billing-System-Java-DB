    private void bttBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBuscarActionPerformed
        String op = JOptionPane.showInputDialog("Ingrese correlativo de factura que desea MODIFICAR");
        
        DetalleFactura_3597 F = new DetalleFactura_3597(); 
        F.setCodigo(parseInt(op));
       
        
        if (ab_Factura3597.BuscarFactura(F)){
            
            DefaultTableModel modelo = (DefaultTableModel) TablaArticulo.getModel();
            modelo.getDataVector().removeAllElements();
            TablaArticulo.updateUI();
            
            E = ab_Empleado.A_invCajero();
            D = ab_Factura3597.A_invFactura();
            C = ab_Cliente3597.A_inv();
            
            int pf = ab_Factura3597.Verificar_Factura(F);
            int pe = ab_Empleado.VCajeroFac(D.get(pf).getIDempleado());
            int pc = ab_Cliente3597.VClienteFac(Integer.toString(D.get(pf).getNITcliente()));
            
            lbCorrelativo.setText(op);
            lbFecha.setText(D.get(pf).getFecha());
            txNumero_Caja.setText(Integer.toString(D.get(pf).getCaja()));
            txIDcajero.setText(Integer.toString(D.get(pf).getIDempleado()));
            lbNombreEmpleado.setText(E.get(pe).getNombre());
            lbCodEmpleado.setText(Integer.toString(E.get(pe).getCodigo()));
            txNITcliente.setText(Integer.toString(D.get(pf).getNITcliente()));
            lbNombreCliente.setText(C.get(pc).getNombre());
            lbCodigoCliente.setText(Integer.toString(C.get(pc).getCodigo()));
            
//            DefaultTableModel modelo = (DefaultTableModel) TablaArticulo.getModel();
            Object[] fila = new Object[5];
            
            for (Articulo_3597 f : D.get(pf).getArrayArticulos()) {
                fila[0] = f.getCodigo();
                fila[1] = f.getCantidad();
                fila[2] = f.getNombre();
                fila[3] = f.getPrecioUnitario();
                fila[4] = f.getDescripción();
                modelo.addRow(fila);
            }
        }
    }//GEN-LAST:event_bttBuscarActionPerformed

