    private void LimpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();
        modelo.getDataVector().removeAllElements();
        TablaProveedor.updateUI();
        
        TableData();
    }
    
    private void TableData(){

        String sql = "";
        sql = "select * from Proveedores;";
            
        try {
            Statement comando = c_conexion3597.establecerConexion().createStatement();
            ResultSet registro = comando.executeQuery(sql);

            while (registro.next()){
                String codigo = registro.getString("codigo");
                String nombre = registro.getString("nombre");
                String NIT = registro.getString("NIT");
                String direccion = registro.getString("direccion");
                String telefono = registro.getString("telefono");

                DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();
                Object[] fila = new Object[5];
                fila[0] = codigo;
                fila[1] = nombre;
                fila[2] = NIT;
                fila[3] = direccion;
                fila[4] = telefono;
                modelo.addRow(fila); 
            }

        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            c_conexion3597.closeConexion();
        }
    }
    
    
