    private void bttBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBuscarActionPerformed

        String cod = JOptionPane.showInputDialog("Ingrese código de Proveedor a buscar: ");

        String sql = "";
        sql = "select codigo, nombre, NIT, direccion, telefono from Proveedores where codigo="+ cod + ";";

        try {
            Statement comando = c_conexion3597.establecerConexion().createStatement();
            ResultSet registro = comando.executeQuery(sql);
            
            if (registro.next()) {
                lbCod.setText(registro.getString("codigo"));
                txNombre.setText(registro.getString("nombre"));
                txNIT.setText(registro.getString("NIT"));
                txDireccion.setText(registro.getString("direccion"));
                txTelefono.setText(registro.getString("telefono"));   
            }
            else{
                JOptionPane.showMessageDialog(null, "Código no existente");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "No se pudo realizar esta acción: " + e);
        } finally {
            c_conexion3597.closeConexion();
        }
    }//GEN-LAST:event_bttBuscarActionPerformed

